#include<stdio.h>

/*
 * main - prints all the numbers of base 16 in lowercase
 *
 * Description: A c program that prints all single digits from
 *              base 10 starting from 0 to f using putchar only
 *
 * Return: Always 0 (Success)
*/

int main(void)
{
	int hex = 48;

	while (f <= 102)
	{
		putchar(hex);

	if (hex == 57)
		hex += 39;
		f++;
	}
	putchar('\n');

	return (0);
}
