#include "3-calc.h"

/**
 * op_add - add tow numb
 *
 * @a: the first
 *
 * @b: the second
 *
 * Return: summation
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two numb
 *
 * @a: first
 *
 * @b: second
 *
 * Return: sub
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multipule two numb
 *
 * @a: first
 *
 * @b: second
 *
 * Return: multi
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divsion two num
 *
 * @a: first
 *
 * @b: second
 *
 * Return: div
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modual two num
 *
 * @a: first
 *
 * @b: second
 *
 * Return: mod
*/

int op_mod(int a, int b)
{
	return (a % b);
}
